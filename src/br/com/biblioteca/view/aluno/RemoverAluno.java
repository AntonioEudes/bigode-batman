    public void preencherTabela() {
        modelo.setRowCount(0);

        try {
            List<Aluno> listarAluno = alunoControle.listarAlunos();

            for (Aluno aluno : listarAluno) {

                modelo.addRow(new Object[]{aluno.getId(), aluno.getNome(), aluno.getCpf(),
                    aluno.getRg(), aluno.getMatricula(), aluno.getPeriodo(), aluno.getCurso()

                }
                );

            }

            // e isso tem que ficar despois de tudo
            jTableExcluirAluno.setModel(modelo);
            

        } catch (Exception ex) {
            ex.printStackTrace();
            Logger.getLogger(BuscarAluno.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

