    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        if (jTableExcluirAluno.getSelectedRow() == -1) {
        JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SELECIONE_ELEMENTO_TABELA,"ALuno"));            
        // JOptionPane.showMessageDialog(null, "Selecione um elemento da tabela!");
        } else {
            int linha = jTableExcluirAluno.getSelectedRow();

            int idAluno = (Integer) modelo.getValueAt(linha, 0);
            
            try {
                //  }
            
                
               // int resp = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Remover esse Aluno ?", "removido", JOptionPane.YES_NO_OPTION);
                JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_PERGUNTA_REMOCAO_TABELA,"Aluno?"));
                alunoControle.RemoverAluno(idAluno);
                preencherTabela();
                
            
                
                JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_REMOVIDO_SUCESSO,"Aluno"));
                
            } catch (Exception ex) {
                Logger.getLogger(RemoverAluno.class.getName()).log(Level.SEVERE, null, ex);
            }
        }

    }//GEN-LAST:event_jButtonExcluirActionPerformed

