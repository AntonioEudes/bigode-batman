    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        Funcionario f = new Funcionario();
        
        //int linha =  jTableEditarFuncionario.getSelectedRow();
        f.setId(id);
        f.setNome(jTextFieldNome.getText());
        f.setCpf(jFormatteCpf.getText());
        f.setRg(jFormatteRg.getText());
        f.setCargo(jTextFieldCargo.getText());
        
        //f.setId((Integer) jTableEditarFuncionario.getValueAt(linha, 0));
        
        String data = jFormatteDataAdmição.getText();
        DateUtil datautil = new DateUtil();
         Date dtnasc = datautil.stringToDate(data);
        f.setDataAdmicao(dtnasc);
        try {
            funcionarioControle.EditarFuncionario(f);
        } catch (Exception ex) {
            Logger.getLogger(EditarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }
        
        int resp = JOptionPane.showConfirmDialog(null, "Tem certeza que deseja editado esse Funcionario ?", "editar", JOptionPane.YES_NO_OPTION);
            if (resp == 0) 
        JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SUCESSO_EDITAR_GENERICA,"Funcionario"));
        
        jTextFieldNome.setText("");
        jFormatteCpf.setText("");
        jFormatteRg.setText("");
        jTextFieldCargo.setText("");
        jFormatteDataAdmição.setText("");
        preencherTabela();
        
        
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

