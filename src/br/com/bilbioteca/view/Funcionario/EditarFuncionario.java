    public void preencherTabela() {
        
        modelo.setRowCount(0);
         
        try {
            List<Funcionario> listarFuncionario = new ArrayList<Funcionario>();
            
            listarFuncionario = funcionarioControle.listarFuncionario();
            

            for (Funcionario funcinario : listarFuncionario) {
                
                modelo.addRow(new Object[]{funcinario.getId(), funcinario.getNome(), funcinario.getCpf(),
                    funcinario.getRg(), funcinario.getCargo(), funcinario.getDataAdmicao()

                }
                );

            }

            // e isso tem que ficar despois de tudo
            jTableEditarFuncionario.setModel(modelo);

        } catch (Exception ex) {
            ex.printStackTrace();
            Logger.getLogger(BuscarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

