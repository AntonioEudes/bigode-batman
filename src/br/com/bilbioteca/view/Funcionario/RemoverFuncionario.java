    private void jButtonExcluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonExcluirActionPerformed

        if (jTableExcluirFuncionario.getSelectedRow() == -1) {
            JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_SELECIONE_ELEMENTO_TABELA,"Funcionario"));
           // JOptionPane.showMessageDialog(null, "Selecione um elemento da tabela!");
        } else {
            int linha = jTableExcluirFuncionario.getSelectedRow();
            
            int idAluno = (Integer) modelo.getValueAt(linha, 0);
            
            try {
                JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_PERGUNTA_REMOCAO_TABELA,"Funcionario?"));
                        
                        //JOptionPane.showConfirmDialog(null, "Tem certeza que deseja Remover esse Funcionario ?", "removido", JOptionPane.YES_NO_OPTION);
            
               
                funcionarioControle.RemoverFuncionario(idAluno);
                preencherTabela();
                JOptionPane.showMessageDialog(null, MensagensUtil.getValor(MensagensUtil.MSG_REMOVIDO_SUCESSO,"Funcionario"));
                
            } catch (Exception ex) {
                Logger.getLogger(RemoverFuncionario.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
    }//GEN-LAST:event_jButtonExcluirActionPerformed

