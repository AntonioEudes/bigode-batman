    public void preencherTabela() {
        
        modelo.setRowCount(0);
        DefaultTableModel modelo = new DefaultTableModel();

        //tava faltando essa linha ai
        modelo = (DefaultTableModel) jTableExcluirFuncionario.getModel();

        try {
            List<Funcionario> lista = funcionarioControle.listarFuncionario();
            

            for (Funcionario funcinario : lista) {

                modelo.addRow(new Object[]{funcinario.getId(), funcinario.getNome(), funcinario.getCpf(),
                    funcinario.getRg(), funcinario.getCargo(),funcinario.getDataAdmicao()

                }
                );

            }

            // e isso tem que ficar despois de tudo
            jTableExcluirFuncionario.setModel(modelo);

        } catch (Exception ex) {
            ex.printStackTrace();
            Logger.getLogger(BuscarFuncionario.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

  

